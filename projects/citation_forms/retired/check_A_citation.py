for lexeme, metadata in sorted_items(lexemes):
    if "A" not in metadata["pos"]:
        continue

    if "full-citation-form" in metadata and lexeme not in SKIP:
        lexeme = lexeme.split("/")[0]
        citation_form = metadata["full-citation-form"]
        print("{}: {}".format(lexeme, citation_form))
        for alt in citation_form.split(" / "):
            components = alt.split(", ")
            assert len(components) in [2, 3]
            assert citation_form.endswith((
                "ος, ον",
                "ός, όν",
                "ης, ες",
                "ής, ες",
                "ής, ές",
                "ων, ον",
                "ής, ές, gen. οῦς",
                "ής, ές, οῦς",
                "ην, εν, gen. ενος",
                "ων, ον, gen. ονος",
                "πους, ουν, gen. ποδος",

                "ος, η, ον",
                "ός, ή, όν",
                "ος, α, ον",
                "ός, ά, όν",
                "ος, έρα, ον",
                "οι, αι, α",
                "ας, ασα, αν",
                "οῦς, ῆ, οῦν",
                "οῦς, ᾶ, οῦν",
                "ύς, εῖα, ύ",
                "ών, οῦσα, όν",
                "υς, εια, υ",
                "τερος, τέρα, ον",
            ))
