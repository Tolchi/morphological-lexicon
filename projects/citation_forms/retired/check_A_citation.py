    "λοίδορος", "λοιμός", "μέγας", "μέγιστος", "μείζων", "μέλας", "μηδείς",
    "μισθωτός", "μοιχαλίς", "νεκρός", "νῆστις", "ὀγδοήκοντα", "οἰκεῖος",
    "ὀκτώ", "οὐδείς", "παρείσακτος", "παρόμοιος", "πᾶς", "πάσχα", "πένης",
    "πέντε", "πεντήκοντα", "πετεινόν", "Πισιδία", "πλανήτης", "πλατεῖα",
